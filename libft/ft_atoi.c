#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		t;
	long	a;

	t = 1;
	i = 0;
	a = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			t *= -1;
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		a = (a * 10) + (str[i] - 48);
		i++;
		if (a * t < -2147483648)
			return (0);
		if (a * t > 2147483647)
			return (-1);
	}
	return (t * a);
}
